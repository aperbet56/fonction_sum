// Déclaration de la fonction de sum qui va permettre additionner deux nombres
const sum = (a, b) => {
  return a + b;
};

// Utilisation de la fonction et affichage du résultat dans la console
const num1 = 24;
const num2 = 100;
const result = sum(num1, num2);

console.log(`La somme de ${num1} et ${num2} est : ${result}`);
